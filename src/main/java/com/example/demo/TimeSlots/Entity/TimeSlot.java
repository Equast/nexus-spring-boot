package com.example.demo.TimeSlots.Entity;

public class TimeSlot {
    private String _id;       // Unique identifier for the timeslot
    private String startTime; // Start time
    private String endTime;   // End time
    private String startDate; // Optional start date
    private String endDate;   // Optional end date

    public TimeSlot() {
    }

    public String get_id() {
        return _id;
    }

    public void set_id(String _id) {
        this._id = _id;
    }

    public String getStartTime() {
        return startTime;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    public String getEndTime() {
        return endTime;
    }

    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getEndDate() {
        return endDate;
    }

    public void setEndDate(String endDate) {
        this.endDate = endDate;
    }
}
